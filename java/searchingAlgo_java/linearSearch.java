import java.util.*;

public class linearSearch{
    public static void main(String[] args) {
        int[] arr = {1,2,3,4};
        search(arr,3);
    }

    static int search(int[] arr,int x){
        for(int i =0;i<arr.length;i++){
            if(arr[i] == x){
                return i;
            }
        }
        return -1;
    }
}